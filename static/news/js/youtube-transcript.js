/**
 * YouTube transcript fetcher via Cloudflare Workers proxy.
 */
(function initYoutubeTranscript(global) {
  "use strict";

  const PROXY_BASE_URL = "https://vibe-speak-proxy.kishineseiichi.workers.dev/";
  const VIDEO_ID_RE = /^[a-zA-Z0-9_-]{11}$/;
  const DEFAULT_LANGUAGES = ["en", "ja"];
  const DEFAULT_TIMEOUT_MS = 30000;
  const DEFAULT_MAX_RETRIES = 2;
  const LANGUAGE_LABELS = { ja: "Japanese", en: "English" };

  function extractVideoId(input) {
    const raw = String(input || "").trim();
    if (!raw) throw new Error("YouTube URL または動画 ID を入力してください。");
    if (VIDEO_ID_RE.test(raw)) return raw;

    const decoded = decodeURIComponent(raw.split("#")[0]);
    const match = decoded.match(/(?:v=|vi=|\/)([0-9A-Za-z_-]{11})(?=$|[^0-9A-Za-z_-])/i);
    if (match && VIDEO_ID_RE.test(match[1])) return match[1];

    throw new Error(
      "有効な YouTube URL または 11 桁の動画 ID を入力してください。（watch?v= / youtu.be/ / shorts/ に対応）"
    );
  }

  function languageLabel(code) {
    const base = String(code || "").split("-")[0].toLowerCase();
    return LANGUAGE_LABELS[base] || code || "Unknown";
  }

  function langMatches(candidate, preferred) {
    const c = String(candidate || "").toLowerCase();
    const p = String(preferred || "").toLowerCase();
    return c === p || c.startsWith(`${p}-`);
  }

  function normalizeSnippet(snippet) {
    const text = String(snippet?.text || snippet?.content || "").trim();
    if (!text) return null;

    let start = Number(snippet.start ?? snippet.startSec ?? snippet.offset ?? 0);
    let duration = Number(snippet.duration ?? snippet.dur ?? snippet.length ?? 0);

    if (snippet.start_ms != null || snippet.tStartMs != null) {
      start = Number(snippet.start_ms ?? snippet.tStartMs ?? 0) / 1000;
    }
    if (snippet.d_duration_ms != null || snippet.dDurationMs != null) {
      duration = Number(snippet.d_duration_ms ?? snippet.dDurationMs ?? 0) / 1000;
    }

    return {
      start: Math.round(start * 1000) / 1000,
      duration: Math.round(Math.max(duration, 0.1) * 1000) / 1000,
      text,
    };
  }

  function normalizeSnippets(snippets) {
    return (snippets || []).map(normalizeSnippet).filter(Boolean);
  }

  function parseTimedTextEvents(data) {
    const snippets = [];
    for (const event of data?.events || []) {
      if (!event?.segs) continue;
      const text = event.segs
        .map((seg) => seg.utf8 || "")
        .join("")
        .replace(/\n/g, " ")
        .trim();
      if (!text) continue;
      snippets.push({
        start: Number(event.tStartMs || 0) / 1000,
        duration: Math.max(Number(event.dDurationMs || 0) / 1000, 0.1),
        text,
      });
    }
    return snippets;
  }

  function parseScrapingBeeRuns(entry) {
    const runs = entry?.snippet?.runs || [];
    return runs
      .map((run) => String(run?.text || "").trim())
      .filter(Boolean)
      .join(" ");
  }

  function parseScrapingBeeEntries(entries) {
    return (entries || [])
      .map((entry) =>
        normalizeSnippet({
          start_ms: entry.start_ms,
          d_duration_ms: entry.d_duration_ms,
          text: parseScrapingBeeRuns(entry),
        })
      )
      .filter(Boolean);
  }

  function pickScrapingBeeTrack(subtitlesRoot, languages) {
    const manual = subtitlesRoot?.uploader_provided || {};
    const automatic = subtitlesRoot?.auto_generated || {};

    for (const lang of languages) {
      if (manual[lang]?.length) return { languageCode: lang, isGenerated: false, snippets: parseScrapingBeeEntries(manual[lang]) };
      for (const [key, entries] of Object.entries(manual)) {
        if (langMatches(key, lang) && entries?.length) {
          return { languageCode: key, isGenerated: false, snippets: parseScrapingBeeEntries(entries) };
        }
      }
    }

    for (const lang of languages) {
      if (automatic[lang]?.length) return { languageCode: lang, isGenerated: true, snippets: parseScrapingBeeEntries(automatic[lang]) };
      for (const [key, entries] of Object.entries(automatic)) {
        if (langMatches(key, lang) && entries?.length) {
          return { languageCode: key, isGenerated: true, snippets: parseScrapingBeeEntries(entries) };
        }
      }
    }

    return null;
  }

  function selectCaptionTrack(tracks, preferredLanguages, includeAutoGenerated) {
    if (!tracks?.length) return null;
    const manual = tracks.filter((track) => track.kind !== "asr");
    const auto = tracks.filter((track) => track.kind === "asr");
    const searchOrder = includeAutoGenerated ? [...manual, ...auto] : manual;

    for (const lang of preferredLanguages) {
      const exact = searchOrder.find((track) => langMatches(track.languageCode, lang));
      if (exact) return exact;
    }
    return searchOrder[0] || null;
  }

  function parseCaptionTrackEvents(track) {
    if (Array.isArray(track?.events)) return parseTimedTextEvents(track);
    if (Array.isArray(track?.snippets)) return normalizeSnippets(track.snippets);
    if (Array.isArray(track?.segments)) return normalizeSnippets(track.segments);
    return [];
  }

  function parseProxyPayload(rawText, videoId, languages) {
    const trimmed = String(rawText || "").trim();
    if (!trimmed) throw new Error("プロキシから空のレスポンスが返されました。");

    let data;
    try {
      data = JSON.parse(trimmed);
    } catch (_err) {
      if (trimmed.includes("WEBVTT") || trimmed.includes("-->")) {
        return parseVttTranscript(trimmed, videoId, languages);
      }
      throw new Error(`プロキシのレスポンスを解析できませんでした: ${trimmed.slice(0, 120)}`);
    }

    if (data?.ok === false || data?.error) {
      throw new Error(String(data.error || data.message || "字幕の取得に失敗しました。"));
    }

    if (
      typeof data?.language_code === "string" &&
      Array.isArray(data?.snippets) &&
      data.snippets.length
    ) {
      return {
        languageCode: data.language_code,
        isGenerated: Boolean(data.is_generated),
        snippets: normalizeSnippets(data.snippets),
      };
    }

    if (Array.isArray(data?.snippets) && data.snippets.length) {
      return {
        languageCode: data.language_code || data.languageCode || "en",
        isGenerated: Boolean(data.is_generated ?? data.isGenerated ?? data.isAutoGenerated),
        snippets: normalizeSnippets(data.snippets),
      };
    }

    if (Array.isArray(data?.segments) && data.segments.length) {
      return {
        languageCode: data.language_code || data.languageCode || data.language || "en",
        isGenerated: Boolean(data.is_generated ?? data.isGenerated ?? data.kind === "asr"),
        snippets: normalizeSnippets(data.segments),
      };
    }

    if (Array.isArray(data?.transcript) && data.transcript.length) {
      return {
        languageCode: data.language_code || data.languageCode || data.language || "en",
        isGenerated: Boolean(data.is_generated ?? data.isGenerated),
        snippets: normalizeSnippets(data.transcript),
      };
    }

    if (Array.isArray(data) && data.length) {
      return {
        languageCode: "en",
        isGenerated: false,
        snippets: normalizeSnippets(data),
      };
    }

    if (data?.events) {
      const snippets = parseTimedTextEvents(data);
      if (!snippets.length) throw new Error("字幕データを解析できませんでした。");
      return {
        languageCode: data.language_code || data.languageCode || data.language || "en",
        isGenerated: Boolean(data.is_generated ?? data.isGenerated ?? data.kind === "asr"),
        snippets,
      };
    }

    if (data?.subtitles) {
      const picked = pickScrapingBeeTrack(data.subtitles, languages);
      if (!picked?.snippets?.length) throw new Error("日本語・英語の字幕が見つかりませんでした。");
      return picked;
    }

    const tracks =
      data?.captions?.playerCaptionsTracklistRenderer?.captionTracks ||
      data?.captionTracks ||
      data?.tracks;
    if (Array.isArray(tracks) && tracks.length) {
      const selected = selectCaptionTrack(tracks, languages, true);
      const snippets = parseCaptionTrackEvents(selected);
      if (!snippets.length && selected?.baseUrl && data?.events) {
        return {
          languageCode: selected.languageCode || "en",
          isGenerated: selected.kind === "asr",
          snippets: parseTimedTextEvents(data),
        };
      }
      if (!snippets.length) throw new Error("日本語・英語の字幕が見つかりませんでした。");
      return {
        languageCode: selected?.languageCode || "en",
        isGenerated: selected?.kind === "asr",
        snippets,
      };
    }

    if (typeof data?.text === "string" && data.text.trim()) {
      return {
        languageCode: data.language_code || data.languageCode || data.language || "en",
        isGenerated: Boolean(data.is_generated ?? data.isGenerated),
        snippets: [{ start: 0, duration: 0.1, text: data.text.trim() }],
      };
    }

    throw new Error("プロキシのレスポンス形式が未対応です。");
  }

  function parseVttTimestamp(value) {
    const raw = String(value || "").trim().replace(",", ".");
    const parts = raw.split(":");
    if (parts.length === 3) {
      return Number(parts[0]) * 3600 + Number(parts[1]) * 60 + Number(parts[2]);
    }
    if (parts.length === 2) {
      return Number(parts[0]) * 60 + Number(parts[1]);
    }
    return 0;
  }

  function parseVttTranscript(text, videoId, languages) {
    const snippets = [];
    const blocks = text.replace(/\r\n/g, "\n").split(/\n\s*\n/);
    for (const block of blocks) {
      const lines = block.split("\n").map((line) => line.trim()).filter(Boolean);
      const timeLine = lines.find((line) => line.includes("-->"));
      if (!timeLine) continue;
      const [startRaw, endRaw] = timeLine.split("-->").map((part) => part.trim());
      const start = parseVttTimestamp(startRaw);
      const end = parseVttTimestamp(endRaw.split(" ")[0]);
      const body = lines
        .filter((line) => line !== timeLine && !/^\d+$/.test(line))
        .join(" ")
        .replace(/<[^>]+>/g, " ")
        .replace(/\s+/g, " ")
        .trim();
      if (!body) continue;
      snippets.push({ start, duration: Math.max(end - start, 0.1), text: body });
    }
    if (!snippets.length) throw new Error("字幕データを解析できませんでした。");
    return {
      languageCode: languages[0] || "en",
      isGenerated: false,
      snippets: normalizeSnippets(snippets),
    };
  }

  function filterSnippetsByRange(snippets, startSec, endSec) {
    if (endSec <= startSec) return snippets;
    return snippets.filter((snippet) => {
      const snippetStart = Number(snippet.start || 0);
      const snippetEnd = snippetStart + Number(snippet.duration || 0);
      return snippetStart < endSec && snippetEnd > startSec;
    });
  }

  function formatTranscriptTimestamp(sec) {
    const s = Math.max(0, Math.floor(Number(sec) || 0));
    const hours = Math.floor(s / 3600);
    const minutes = Math.floor((s % 3600) / 60);
    const seconds = s % 60;
    if (hours) return `${hours}:${String(minutes).padStart(2, "0")}:${String(seconds).padStart(2, "0")}`;
    return `${minutes}:${String(seconds).padStart(2, "0")}`;
  }

  function formatScriptForLesson(snippets) {
    const lines = [];
    let current = [];
    for (const snippet of snippets) {
      const text = String(snippet.text || "").trim();
      if (!text) continue;
      current.push(text);
      if (/[.!?]$/.test(text)) {
        lines.push(current.join(" "));
        current = [];
      }
    }
    if (current.length) lines.push(current.join(" "));
    return lines.join("\n");
  }

  function formatTimedScript(snippets) {
    return snippets
      .map((snippet) => `${formatTranscriptTimestamp(snippet.start)}  ${snippet.text}`)
      .join("\n");
  }

  function translateFetchError(err, statusCode) {
    const message = String(err?.message || err || "").toLowerCase();
    if (err instanceof TypeError || message.includes("failed to fetch") || message.includes("networkerror")) {
      return new Error(
        "字幕プロキシ（Cloudflare Workers）に接続できませんでした。Worker の URL とデプロイ状態を確認してください。"
      );
    }
    if (statusCode === 404 || message.includes("not found")) {
      return new Error("動画が見つからないか、再生できません。");
    }
    if (statusCode === 429 || message.includes("429") || message.includes("rate limit")) {
      return new Error("YouTube へのリクエストが制限されています。しばらく待ってから再試行してください。");
    }
    if (message.includes("blocked") || message.includes("sign in to confirm")) {
      return new Error("YouTube から字幕を取得できませんでした。動画が非公開・年齢制限の可能性があります。");
    }
    if (message.includes("no captions") || message.includes("字幕")) {
      return err instanceof Error ? err : new Error(String(err || "字幕の取得に失敗しました。"));
    }
    return err instanceof Error ? err : new Error(String(err || "字幕の取得に失敗しました。"));
  }

  async function withTimeout(timeoutMs, operation) {
    const controller = new AbortController();
    const timer = setTimeout(() => controller.abort(), timeoutMs);
    try {
      return await operation(controller.signal);
    } finally {
      clearTimeout(timer);
    }
  }

  async function fetchWithRetry(url, options, maxRetries) {
    let lastError = null;
    for (let attempt = 0; attempt < maxRetries; attempt += 1) {
      try {
        const response = await fetch(url, options);
        if ([408, 425, 429, 500, 502, 503, 504].includes(response.status) && attempt < maxRetries - 1) {
          const delayMs = response.status === 429 ? 4000 * 2 ** attempt : 2 ** attempt * 1000;
          await new Promise((resolve) => setTimeout(resolve, delayMs));
          continue;
        }
        return response;
      } catch (err) {
        lastError = err;
        if (attempt >= maxRetries - 1) break;
        await new Promise((resolve) => setTimeout(resolve, 2 ** attempt * 1000));
      }
    }
    throw translateFetchError(lastError || new Error("字幕の取得に失敗しました。"));
  }

  async function fetchTranscriptFromProxy(videoId, languages, timeoutMs, maxRetries) {
    const url = `${PROXY_BASE_URL}?id=${encodeURIComponent(videoId)}`;
    const response = await withTimeout(timeoutMs, (signal) =>
      fetchWithRetry(
        url,
        {
          headers: { Accept: "application/json, text/plain, */*" },
          signal,
        },
        maxRetries
      )
    );

    const body = await response.text();
    if (!response.ok) {
      try {
        const errJson = JSON.parse(body);
        throw new Error(String(errJson.error || errJson.message || `HTTP ${response.status}`));
      } catch (err) {
        if (err instanceof Error && err.message && !err.message.startsWith("Unexpected")) throw err;
        throw translateFetchError(new Error(body.slice(0, 200) || `HTTP ${response.status}`), response.status);
      }
    }

    return parseProxyPayload(body, videoId, languages);
  }

  async function fetchTranscript(videoIdOrUrl, options) {
    const opts = options || {};
    const languages = opts.languages || DEFAULT_LANGUAGES;
    const timeoutMs = opts.timeoutMs || DEFAULT_TIMEOUT_MS;
    const maxRetries = opts.maxRetries || DEFAULT_MAX_RETRIES;
    const startSec = opts.startSec ?? null;
    const endSec = opts.endSec ?? null;

    const videoId = extractVideoId(videoIdOrUrl);
    const parsed = await fetchTranscriptFromProxy(videoId, languages, timeoutMs, maxRetries);
    if (!parsed?.snippets?.length) {
      throw new Error("日本語・英語の字幕が見つかりませんでした。");
    }

    const fetchedSnippets = parsed.snippets;
    let snippets = fetchedSnippets;
    let rangeApplied = false;
    if (startSec !== null && endSec !== null && Number(endSec) > Number(startSec)) {
      snippets = filterSnippetsByRange(fetchedSnippets, Number(startSec), Number(endSec));
      rangeApplied = true;
      if (!snippets.length) {
        throw new Error("指定した時間範囲に文字起こしがありません。");
      }
    }

    const allSnippets = normalizeSnippets(fetchedSnippets);
    const activeSnippets = normalizeSnippets(snippets);

    return {
      video_id: videoId,
      language: languageLabel(parsed.languageCode),
      language_code: parsed.languageCode,
      is_generated: Boolean(parsed.isGenerated),
      script: formatScriptForLesson(activeSnippets),
      script_timed: formatTimedScript(activeSnippets),
      snippets: activeSnippets,
      all_snippets: rangeApplied ? allSnippets : activeSnippets,
      range_applied: rangeApplied,
      start_sec: rangeApplied ? Number(startSec) : null,
      end_sec: rangeApplied ? Number(endSec) : null,
    };
  }

  global.YoutubeTranscript = {
    extractVideoId,
    fetchTranscript,
    filterSnippetsByRange,
    formatScriptForLesson,
    formatTimedScript,
    normalizeSnippets,
  };
})(window);
